func (ci *CrickInfoAPI) GetScoreCard(ctx *gin.Context, seriesID, matchID string) (*ScoreCardResponse, error) {
	url := ci.buildScoreCardSlug(ScoreCard, seriesID, matchID)
	req, err := ci.client.NewRequest(http.MethodGet, url, nil)
	if err != nil {
		fmt.Println("error:", err)
		return nil, err
	}

	var res *ScoreCardResponse
	if _, err := ci.client.Do(context.Background(), req, &res); err != nil {
		fmt.Println("error:", err)
		return nil, err
	}
	return res, nil
}

func (ci *CrickInfoAPI) buildScoreCardSlug(urlPath, seriesID, matchID string) string {
	return fmt.Sprintf("%s&seriesId=%s&matchId=%s", urlPath, seriesID, matchID)
}
